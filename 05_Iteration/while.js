//WHILE LOOP

let index =0;
while (index <=10) {
    //console.log(`Value of ${index}`);
    index=index+2
}


//Array
const myArr=["Apple","Banana","Cherry","Mango"]
let arr=0
while (arr<myArr.length) {
    //console.log(`Value of ${myArr[arr]}`);
    arr=arr+1;
}


// *************************************************DO WHILE LOOP*******************************************************

let score =11;

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);